""""Дано чотири цілих числа, одне з яких відмінно від
трьох інших, рівних між собою.
Вивести порядковий номер цього числа."""
n1 = int(input("Введіть число:"))
n2 = int(input("Введіть число:"))
n3 = int(input("Введіть число:"))
n4 = int(input("Введіть число:"))
if n1 == n2 == n3:
    print("4")
elif n1 == n2 == n4:
    print("3")
elif n1 == n3 == n4:
    print("2")
elif n2 == n3 == n4:
    print("1")
