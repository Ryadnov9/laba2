""""17Дано три цілих числа. Визначте, скільки серед них збігаються.
Програма повинна вивести одне з чисел: 3 (якщо все збігаються), 2 (якщо два збігається)
 або 0 (якщо все числа різні)."""

a = int(input("Введіть число"))
b = int(input("Введіть число"))
c = int(input("Введіть число"))
if a == b == c:
    print(3)

elif a == b or b == c or a == c:
    print(2)

else:
    print(0)

